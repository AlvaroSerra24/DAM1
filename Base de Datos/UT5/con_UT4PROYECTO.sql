CREATE TABLE JUGADORES (
    DNI VARCHAR2(9),
    Nombre VARCHAR2(30) NOT NULL,
    Edad NUMBER(2) NOT NULL,
    Correo VARCHAR2(50),
    Provincia VARCHAR2(50),
    CONSTRAINT DNI_PK PRIMARY KEY(DNI),
    CONSTRAINT correo_UK UNIQUE (Correo)
    );
CREATE TABLE CUENTAS (
    Nick VARCHAR2(30),
    Fecha_creacion DATE NOT NULL,
    DNI_Jug VARCHAR2(9),
    CONSTRAINT Nick_PK PRIMARY KEY(Nick),
    CONSTRAINT DNI_Jug_FK FOREIGN KEY(DNI_Jug) REFERENCES JUGADORES ON DELETE CASCADE
    );
CREATE TABLE JUEGOS (
    Cod_juego NUMBER(10),
    Nombre VARCHAR2(100) NOT NULL,
    Precio NUMBER(5,2) NOT NULL,
    Tipo VARCHAR2(30) NOT NULL,
    Duracion NUMBER (4),
    Compañia_servidor VARCHAR2(30),
    CONSTRAINT Cod_PK PRIMARY KEY(Cod_juego),
    CONSTRAINT Tipo_CK CHECK (Tipo in ('Un jugador', 'Multijugador'))
    );
CREATE TABLE CUENTAS_TIENEN_JUEGOS (
    Nick VARCHAR2(30),
    Cod_juego NUMBER(10),
    Fecha_compra DATE NOT NULL,
    CONSTRAINT Nick_cod_PK PRIMARY KEY(Nick, Cod_juego),
    CONSTRAINT Nick_Acc_FK FOREIGN KEY(Nick) REFERENCES CUENTAS ON DELETE CASCADE,
    CONSTRAINT Cod_jue_FK FOREIGN KEY(Cod_juego) REFERENCES JUEGOS ON DELETE CASCADE
    );
CREATE TABLE LOGROS(
    NOMBRE VARCHAR2(150) NOT NULL,
    PUNTOS NUMBER(3) NOT NULL,
    NUMERO NUMBER(10),
    COD_JUEGO NUMBER(10),
    CONSTRAINT LOG_PK PRIMARY KEY(NUMERO,COD_JUEGO),
    CONSTRAINT LOG_JUE_FK FOREIGN KEY(COD_JUEGO)REFERENCES JUEGOS ON DELETE CASCADE
 );
 CREATE TABLE CUENTAS_CONSIGUEN_LOGROS(
    NICK VARCHAR2(30),
    NUMERO NUMBER(10),
    COD_JUEGO NUMBER(10),
    FECHA_LOGRO DATE  NOT NULL,
    CONSTRAINT CON_PK PRIMARY KEY (NICK,NUMERO,COD_JUEGO),
    CONSTRAINT CON_CUE_FK FOREIGN KEY(NICK)REFERENCES CUENTAS ON DELETE CASCADE,
    CONSTRAINT CON_LOG_FK FOREIGN KEY(NUMERO,COD_JUEGO)REFERENCES LOGROS ON DELETE CASCADE
);
CREATE TABLE COMPETICIONES(
    COD_COMPETICIÓN NUMBER(10),
    NOMBRE VARCHAR2(30) NOT NULL,
    FECHA_COMPETICIÓN DATE NOT NULL,
    CONSTRAINT COM_COD_PK PRIMARY KEY (COD_COMPETICIÓN)
);
CREATE TABLE PARTICIPACIONES(
    DNI VARCHAR2(9),
    COD_COMPETICIÓN NUMBER(10),
    COD_JUEGO NUMBER(10),
    PUESTO NUMBER(3) NOT NULL,
    CONSTRAINT PAR_PK PRIMARY KEY (DNI,COD_COMPETICIÓN,COD_JUEGO),
    CONSTRAINT PAR_JUG_FK FOREIGN KEY (DNI)REFERENCES JUGADORES ON DELETE CASCADE,
    CONSTRAINT PAR_COM_FK FOREIGN KEY(COD_COMPETICIÓN)REFERENCES COMPETICIONES ON DELETE CASCADE,
    CONSTRAINT PAR_JUE_FK FOREIGN KEY (COD_JUEGO)REFERENCES JUEGOS ON DELETE CASCADE
);


