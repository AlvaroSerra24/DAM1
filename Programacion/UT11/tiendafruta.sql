
DROP TABLE PERSONA;
DROP TABLE FACTURA;

CREATE TABLE PERSONA(
  DNI VARCHAR(9) NOT NULL,
  NOMBRE VARCHAR(255) NOT NULL,
  APELLIDOS VARCHAR(255) NOT NULL,
  DIRECCION VARCHAR(255),
  TURNO INTEGER,
  PRIMARY KEY (DNI)
  
);

CREATE TABLE FACTURA(
  CODIGO VARCHAR(15),
  CODIGOPEDIDO INTEGER,
  DNI VARCHAR(9) NOT NULL,
  TOTAL NUMERIC(6,2), 
  PRIMARY KEY (CODIGO)
);

